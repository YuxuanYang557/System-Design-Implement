    public void populate(){
        int rowCount = sponsorTbl.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)sponsorTbl.getModel();
        for(int i = rowCount -1; i>=0; i--){
            dtm.removeRow(i);
        }
        
        for(User c: enterprise.getUserList().getUserList()){
            Object row[] = new Object[dtm.getColumnCount()];
            
            row[0]= c.getUserName();
            row[1]= c.getPassword();
            dtm.addRow(row);
        }
    }

