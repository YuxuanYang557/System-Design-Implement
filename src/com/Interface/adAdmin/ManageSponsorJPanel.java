    private void deleteCatagoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCatagoryBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = sponsorTbl.getSelectedRow();
        if(selectedRow >=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                enterprise.getUserList().getUserList().remove(selectedRow);
                db4o.storeSystem(ecosystem);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row before delete", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteCatagoryBtnActionPerformed

