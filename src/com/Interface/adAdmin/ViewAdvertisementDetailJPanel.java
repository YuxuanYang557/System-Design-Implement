    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        // TODO add your handling code here:
        if(fundTF.getText() == null){
            JOptionPane.showMessageDialog(null, "Please input a funds before you approve");
        }else{
            double funds= Double.parseDouble(fundTF.getText());
            String comment = spCommentTF.getText();
            
            ad.getForm().setFunds(funds);
            ad.getForm().setAdvertisementComment(comment);
            ad.setStatus("pfAdmin checking");
            User pfAdmin = (User)pfAdminCB.getSelectedItem();
            ad.setPlatformAdmin(pfAdmin);
            pfAdmin.getAdvertisementList().getAdertisements().add(ad);
            db4o.storeSystem(ecosystem);
            System.out.println(pfAdmin.getAdvertisementList().getAdertisements().get(0).getId());
            JOptionPane.showMessageDialog(null, "you have approved this advertisement apply");
            
            rightPanel.remove(this);
            CardLayout layout = (CardLayout)rightPanel.getLayout();
            layout.previous(rightPanel);
        }
        
    }//GEN-LAST:event_approveBtnActionPerformed

