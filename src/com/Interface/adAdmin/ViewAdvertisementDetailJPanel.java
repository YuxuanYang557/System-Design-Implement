    private void declineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_declineActionPerformed
        // TODO add your handling code here:
         if(spCommentTF.getText() == null){
            JOptionPane.showMessageDialog(null, "Please write down the reason why you reject the apply");
        }else{
            
            String comment = spCommentTF.getText();
            
           
            ad.getForm().setAdvertisementComment(comment);
            ad.setStatus("Reject by adAdmin");
            db4o.storeSystem(ecosystem);
            JOptionPane.showMessageDialog(null, "you have declined this advertisement apply");
            
            rightPanel.remove(this);
            CardLayout layout = (CardLayout)rightPanel.getLayout();
            layout.previous(rightPanel);
        }
    }//GEN-LAST:event_declineActionPerformed

