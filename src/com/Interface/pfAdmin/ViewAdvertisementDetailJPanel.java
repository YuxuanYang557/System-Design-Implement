    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        // TODO add your handling code here:
        if(!ad.getStatus().equals("Waiting for apply")){
        String comment = adCommentTF.getText();
        
        ad.getForm().setPlatformComment(comment);
        ad.setStatus("Waiting for apply");
       
        
        
        
        
        //传最终合格ad到post
        db4o.storeSystem(ecosystem);
        JOptionPane.showMessageDialog(null, "successfully approved");
        }else{
            JOptionPane.showMessageDialog(null, "You already approved this request");
        }
        
    }//GEN-LAST:event_approveBtnActionPerformed

