    private void declineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_declineBtnActionPerformed
        // TODO add your handling code here:
        if(pfCommentTF.getText() == null){
            JOptionPane.showMessageDialog(null, "Please write down the reason why you reject the apply");
        }else{
            
            String comment = pfCommentTF.getText();
            //被decline的广告要从该PfAdmin的广告list中删除
            //user.getAdvertisementList().getAdertisements().remove(ad);
            ad.getForm().setPlatformComment(comment);
            ad.setStatus("Reject by pfAdmin");
            db4o.storeSystem(ecosystem);
            JOptionPane.showMessageDialog(null, "You have declined this advertisement apply");
            
            rightPanel.remove(this);
            CardLayout layout = (CardLayout)rightPanel.getLayout();
            layout.previous(rightPanel);
        }
    }//GEN-LAST:event_declineBtnActionPerformed


